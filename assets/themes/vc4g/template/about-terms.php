        </a>
      </h4>
    </div>
    <div id="collapseKarat" class="panel-collapse collapse in" role="tabpanel" aria-labelledby="headingKarat">
      <div class="term-body">
        <p>The first thing we do is sort out items that are not gold or silver. To do this we use a Rare Earth Magnet. The Rare Earth Magnet is an incredibly powerful permanent magnet made from alloys of rare earth elements. Gold, Silver, and Platinum are not magnetic. This catches some plated items immediately. Unfortunately we are unable to purchase plated pieces as the cost of refining them exceeds the value of the metal we can extract from the item. However, there are many non-valuable metals that are also non-magnetic.</p>
        <p>The second step in sorting involves checking for stamps. Many stamps are visible to the naked eye but some are stamped on the tiny posts of earrings or are too worn down to make out. In this case we use a jeweller's loop to closely examine each piece. Stamps tell you what the item is sold as and is a fairly reliable way of identifying the gold purity. However, it is not rare to find an item stamped higher than its actual purity.</p>
