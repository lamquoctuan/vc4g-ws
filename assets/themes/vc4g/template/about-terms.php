        <p>Some large items may not be solid gold or silver. These items are tricky as they can be thickly plated, filled with weights or something of the like. A final test before payment may be required by the buyer to confirm the item is not fraudulent. This can involve cutting the piece and damaging the piece beyond repair. We will ask you before these types of tests. Essentially we have agreed to pay the quoted price if the item is not fraudulent. If you do not wish to have the test performed on your item, we will be happy to return it to you, but will not be able to purchase the item.</p></div>
