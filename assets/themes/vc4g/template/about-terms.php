    <div id="collapseWeighing" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingWeighing">
      <div class="term-body">
        <p>Once all the items have been sorted and verified by karat, we begin weighing the items on a scale. The scale is calibrated twice daily and you can ask to have it recalibrated if you like. Our scale will show you the true weight of your items in grams. Typically we weigh all items identified at a particular karat together but can also weigh individual items for you.</p>
        <p>If items have stones or inserts we will subtract the estimated weight of those stones. Gems the size of 1 carat diamonds will weigh approximately 0.2 grams. We assess the rough size and number of stones in the items and subtract this weight from our estimate. For example, if a piece has 4 small stones of the 1/10th carat size we will subtract 4 times 1/10th of 0.2 grams (ie. 0.08 grams).</p></div>
